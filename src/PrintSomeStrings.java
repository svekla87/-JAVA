public class PrintSomeStrings {
    public static void main(String[] args) {
        System.out.println("Строка 1" + System.lineSeparator() + "Строка 2");
        System.out.println("Строка 1\nСтрока 2");

        System.out.println("\"30\" августа 1903г.");
        System.out.println("C:\\Windows\\System32\\Drivers\\etc\\hosts");

        int x = 5 * 8 - 9 / 7 + 5 - 8;
        System.out.println("Результат = " + x);
    }
}
